var day = "Sunday";
switch (day) {
    case 'Monday':
        console.log("Do hard things");
        break;
    case 'Tuesday':
        console.log("Amazing");
        break;
    case 'Wednesday':
        console.log("awesome");
        break;
    case 'Thursday':
        console.log("Beautiful");
        break;
    case 'Friday':
        console.log("Thank God it is Friday");
        break;
    case 'Saturday':
        console.log("Let's have fun");
        break;
    case 'Sunday':
        console.log("Go and pray to God");
        break;
    default:
        console.log("There is no such day");
        break;

}